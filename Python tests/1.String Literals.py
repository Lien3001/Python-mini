#STRING LITERALS

#basic output
print('Hello World')
print('\n')     #Newline for formatting

#Quotation variations
print("Hello World")
print('Hello World')
print('\n')     #Newline for formatting

#Double quote marks for string literals with apostophes
print("I'm here")
print('\n')     #Newline for formatting

#Single quote a string literal that contains double quotes
print('You need to read "Hamlet" by tomorrow')
print('\n')     #Newline for formatting

#Triple single or double quotes to contain anything in a strig literal
print('''I'm reading "Hamlet" tonight''')
print("""I'm reading "Hamlet" tonight""")
print('\n')     #Newline for formatting

#Triple quotes for multiline strings
print("""One
Two
Three""")
print('''Four
Five
Six''')

